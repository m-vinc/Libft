#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int r;

	if (s1 && s2)
	{
		r = ft_strcmp(s1, s2);
		if (r == 0)
			return (1);
		return (0);
	}
	return (0);
}
